print("For-Loop tutorial: Printing natural numbers:-")
for i in range(1, 11):
    print(i, end=" ")
    if i == 10:
        print("\n")

print("For-Loop tutorial: Printing whole numbers:-")
for j in range(11):
    print(j, end=" ")
    if j == 10:
        print("\n")

print("While-Loop tutorial:-")
x = 3
while x > 0:
    print("x =", x)
    x -= 1
